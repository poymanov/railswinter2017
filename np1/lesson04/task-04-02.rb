# encoding: utf-8

# Переменные с числами
param01 = 2308
param02 = 13

# Переменная для хранения среднего арифметического
avg = nil

# Подсчет среднего арифметического
avg = (param01 + param02) / 2

# Вывод заданных чисел
puts "Первое число: " + param01.to_s
puts "Второе число: " + param02.to_s

# Вывод среднего арифметического
puts "Среднее: " + avg.to_s